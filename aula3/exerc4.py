# 4.) Faça um programa que peça 4 notas bimestrais e mostre a media

numeroA = int(input("Digite um número 1: "))
numeroB = int(input("Digite um número 2: "))
numeroC = int(input("Digite um número 3: "))
numeroD = int(input("Digite um número 4: "))

media = (numeroA + numeroB + numeroC + numeroD) / 4

print("Resultado da media dos quatro valores", media,sep=": ")
